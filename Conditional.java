public class Conditional {
    public static void main(String[] args) {
        System.out.println("Conditional statement and Loops");
        int salary = 25000;
        System.out.println("Make a program to add a bonus to the salary employee on conditional loops");
        if(salary > 10000){
            salary = salary + 2000;
        }
        else{
            salary = salary + 1000;
        }
        System.out.println("The updated salary is now " +salary);
    }
    
}
